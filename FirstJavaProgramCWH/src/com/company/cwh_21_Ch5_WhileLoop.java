package com.company;

public class cwh_21_Ch5_WhileLoop {
    public static void main(String[] args) {
        System.out.println("Using While Loop");

        int i = 1;
        while(i<=3){
            System.out.println(i);
            i++;
        }
        System.out.println("Finish Running While Loop \n");


        //Again using While loop to print a number from 100 to 200 using loop
        //Problem-1

        int j = 100;
        while(j<=200){
            System.out.println(j);
            j++;
        }
        System.out.println("Finish Running Problem-1");
    }
}
